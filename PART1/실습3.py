# 파이썬 연산자의 이해(관계연산자, 논리연산자)

a = 10
b = 20
# 관계연산자(결과->True, False)
print( a > b )
print( a >= b )
print( a < b )
print( a <= b )
print( a == b )
print( a != b )

# 논리연산자(결과->True, False)
print( a < 10 and b >= 20 )
print( a < 10 or b >= 20 )
print( not a > 10)

