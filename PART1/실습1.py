# 파이썬 자료형과 출력문의 이해, 서식지정자의 이해

a = 10 # 정수(int)
print("%d" %a)
print("{0}".format(a))

b = 15.6 # 실수(float)
print("%f" %b)

c = 'A' # 문자(str)
print("%c" %c)

d = "Hello Python" # 문자열(str)
print("%s" %d)
print("{0}".format(d))

